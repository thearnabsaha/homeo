export function translateRepertory(text: string): string {
  if (!text) return text;

  if (repBn[text]) return repBn[text];

  const lower = text.toLowerCase();
  const lowMap: Record<string, string> = {};
  for (const k of Object.keys(repBn)) {
    lowMap[k.toLowerCase()] = repBn[k];
  }
  if (lowMap[lower]) return lowMap[lower];

  const words = text.split(/(\s+|[,;()\/\-&]+)/);
  let hasTranslation = false;

  const translated = words.map((w) => {
    if (/^[\s,;()\/\-&]+$/.test(w)) return w;
    const wl = w.toLowerCase();
    if (wordBn[wl]) {
      hasTranslation = true;
      return wordBn[wl];
    }
    return w;
  });

  if (hasTranslation) return translated.join("").trim();
  return text;
}

export function toBengaliNumeral(n: number): string {
  const digits = "০১২৩৪৫৬৭৮৯";
  return String(n).replace(/\d/g, (d) => digits[parseInt(d)]);
}
